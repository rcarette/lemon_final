#include "../../include/main.h"

void	ft_nbr_shots(int shots)
{
	write(1, "\n", 1);
	write(1, "\033[32;1m", ft_strlen("\033[32;1m"));
	write(1, "Number of shots : ", 18);
	ft_putnbr(shots);
	write(1, "\033[0;0m", ft_strlen("\033[0;0m"));
	write(1, "\n", 1);
}
